/**
 * Merge seed products with API products
 */
export const mergeProductsWithSeed = (apiProducts) => {
  // Create a map of API products by ID to avoid duplicates
  const apiProductMap = new Map(apiProducts.map(p => [p.id, p]));
  
  // Add seed products, avoiding duplicates
  seedProducts.forEach(seedProduct => {
    if (!apiProductMap.has(seedProduct.id)) {
      apiProductMap.set(seedProduct.id, seedProduct);
    }
  });
  
  // Return merged array sorted by ID
  return Array.from(apiProductMap.values()).sort((a, b) => a.id - b.id);
};
